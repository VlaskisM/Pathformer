"""Local collision repair for predicted paths.

When the neural planner produces a path segment that crosses land,
this module uses A* on the cost map to find a local detour around the obstacle.
Only infeasible segments are repaired — feasible segments are kept as-is.
"""

import numpy as np
from scipy.ndimage import distance_transform_edt


def check_segment_collision(
    p0: np.ndarray, p1: np.ndarray, land_mask: np.ndarray
) -> bool:
    """Check if the straight-line segment from p0 to p1 crosses land.

    Args:
        p0: (2,) start point in grid coords (row, col).
        p1: (2,) end point in grid coords (row, col).
        land_mask: (H, W) binary array, 1.0 = land.

    Returns:
        True if any sampled point along the segment is on land.
    """
    H, W = land_mask.shape
    dist = np.linalg.norm(p1 - p0)
    n_samples = max(int(np.ceil(dist)) * 2, 2)
    ts = np.linspace(0, 1, n_samples)

    for t in ts:
        pt = p0 + t * (p1 - p0)
        r = int(np.clip(np.round(pt[0]), 0, H - 1))
        c = int(np.clip(np.round(pt[1]), 0, W - 1))
        if land_mask[r, c] > 0.5:
            return True
    return False


def build_repair_cost_map(land_mask: np.ndarray) -> np.ndarray:
    """Build a cost map suitable for local A* repair.

    Args:
        land_mask: (H, W) binary array, 1.0 = land.

    Returns:
        (H, W) float64 cost map. Land = inf, water = 1.0 + proximity penalty.
    """
    dt = distance_transform_edt(1.0 - land_mask)
    base_cost = 1.0 + np.clip(1.0 - dt / 10.0, 0, 1) * 0.5
    cost_map = np.where(land_mask > 0.5, np.inf, base_cost)
    return cost_map.astype(np.float64)


def repair_path(
    path_normalized: np.ndarray,
    land_mask: np.ndarray,
    max_repair_attempts: int = 20,
) -> tuple[np.ndarray, int, list[tuple[int, int]]]:
    """Repair infeasible segments in a predicted path using local A*.

    Args:
        path_normalized: (N, 2) path in normalized [0, 1] coordinates (row, col).
        land_mask: (H, W) binary land mask.
        max_repair_attempts: max number of blocks to repair.

    Returns:
        repaired_path: (M, 2) repaired path in normalized [0, 1] coordinates.
        n_repairs: number of blocks that were repaired.
        repair_ranges: list of (start_idx, end_idx) index ranges in the output
            path that were generated by A* repair (inclusive start, exclusive end).
    """
    from skimage.graph import MCP_Geometric

    H, W = land_mask.shape
    scale = np.array([H - 1, W - 1], dtype=np.float64)

    path_grid = path_normalized.astype(np.float64) * scale

    # Identify infeasible segments
    infeasible = []
    for i in range(len(path_grid) - 1):
        if check_segment_collision(path_grid[i], path_grid[i + 1], land_mask):
            infeasible.append(i)

    if not infeasible:
        return path_normalized, 0, []

    cost_map = build_repair_cost_map(land_mask)

    # Group consecutive infeasible segments into contiguous blocks
    blocks = []
    current_start = infeasible[0]
    current_end = infeasible[0]
    for idx in infeasible[1:]:
        if idx == current_end + 1:
            current_end = idx
        else:
            blocks.append((current_start, current_end + 1))
            current_start = idx
            current_end = idx
    blocks.append((current_start, current_end + 1))

    # Repair each block
    n_repairs = 0
    repair_ranges: list[tuple[int, int]] = []
    repaired_segments = []
    prev_end = 0
    out_idx = 0  # running index into the output array

    for block_start, block_end in blocks[:max_repair_attempts]:
        # Keep feasible prefix
        prefix = path_grid[prev_end:block_start + 1]
        repaired_segments.append(prefix)
        out_idx += len(prefix)

        # A* from waypoint[block_start] to waypoint[block_end]
        start_rc = (int(np.clip(np.round(path_grid[block_start, 0]), 0, H - 1)),
                     int(np.clip(np.round(path_grid[block_start, 1]), 0, W - 1)))
        end_rc = (int(np.clip(np.round(path_grid[block_end, 0]), 0, H - 1)),
                   int(np.clip(np.round(path_grid[block_end, 1]), 0, W - 1)))

        # Ensure start and end are on water
        if not np.isfinite(cost_map[start_rc]):
            start_rc = _nearest_water(start_rc, land_mask)
        if not np.isfinite(cost_map[end_rc]):
            end_rc = _nearest_water(end_rc, land_mask)

        if start_rc is None or end_rc is None:
            seg = path_grid[block_start + 1:block_end + 1]
            repaired_segments.append(seg)
            out_idx += len(seg)
        else:
            try:
                mcp = MCP_Geometric(cost_map, fully_connected=True)
                cumulative_costs, _ = mcp.find_costs(starts=[start_rc], ends=[end_rc])
                if np.isfinite(cumulative_costs[end_rc]):
                    local_path = np.array(mcp.traceback(end_rc), dtype=np.float64)
                    seg = local_path[1:]
                    repaired_segments.append(seg)
                    repair_ranges.append((out_idx, out_idx + len(seg)))
                    out_idx += len(seg)
                    n_repairs += 1
                else:
                    seg = path_grid[block_start + 1:block_end + 1]
                    repaired_segments.append(seg)
                    out_idx += len(seg)
            except Exception:
                seg = path_grid[block_start + 1:block_end + 1]
                repaired_segments.append(seg)
                out_idx += len(seg)

        prev_end = block_end + 1

    # Append remaining feasible suffix
    if prev_end < len(path_grid):
        repaired_segments.append(path_grid[prev_end:])

    repaired_grid = np.concatenate(repaired_segments, axis=0)
    repaired_normalized = repaired_grid / scale

    return repaired_normalized.astype(np.float32), n_repairs, repair_ranges


def _nearest_water(rc: tuple, land_mask: np.ndarray, radius: int = 10) -> "tuple | None":
    """Find the nearest water cell within a given radius (by Euclidean distance)."""
    H, W = land_mask.shape
    r, c = rc
    best = None
    best_dist = float("inf")
    for dr in range(-radius, radius + 1):
        for dc in range(-radius, radius + 1):
            nr, nc = r + dr, c + dc
            if 0 <= nr < H and 0 <= nc < W and land_mask[nr, nc] < 0.5:
                d = dr * dr + dc * dc
                if d < best_dist:
                    best_dist = d
                    best = (nr, nc)
    return best
